# Вычислите среднее арифметическое чисел: а) 1, 2, 3, 4, 5; б) 1, 2, 3, 4, 10; в) 1, 2, 3, 4, 100; г) 1, 2, 3, 4, 1000.
# a
row = [1,2,3,4,5]
print(sum(row) / len(row)) # 3
# б 
row = [1,2,3,4,10]
print(sum(row) / len(row)) # 4
# в 
row = [1,2,3,4,100]
print(sum(row) / len(row)) # 22
# г 
row = [1,2,3,4,1000]
print(sum(row) / len(row)) # 202